package PracticaEjeciciosWhileClase2;

public class Ejercicio_37 {
    //Programa Java que muestre los números del 1 al 10 utilizando la instrucción while
    public static void main(String[] args){
        int num = 0;
        while (num<10){
            num++;
            System.out.print(num + " ");
        }

    }
}
