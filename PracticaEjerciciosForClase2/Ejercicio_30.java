package PracticaEjerciciosForClase2;

public class Ejercicio_30 {
    //Mostar los números entre 90 y 100, de forma decreciente. Ej. 100, 99, 98….. 90
    public static void  main(String[] args){
        int j= 101;
        int i = 0;
        for(i=90;i<=100;i++){
            j--;
            System.out.print(i+" ");
        }
    }
}
